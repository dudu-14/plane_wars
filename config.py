#!/usr/bin/env python3
# -*- coding: utf-8 -*-
"""游戏配置文件模块。

本模块包含飞机大战游戏的所有配置常量和参数设置。
所有的游戏平衡性参数都在这里定义，便于调整和维护。

典型用法示例:
    from config import SCREEN_WIDTH, SCREEN_HEIGHT, PLAYER_SPEED

    # 使用配置参数
    screen = pygame.display.set_mode((SCREEN_WIDTH, SCREEN_HEIGHT))
    player.move(PLAYER_SPEED)
"""

# =============================================================================
# 屏幕和显示设置
# =============================================================================

SCREEN_WIDTH: int = 800  # 游戏窗口宽度（像素）
SCREEN_HEIGHT: int = 600  # 游戏窗口高度（像素）
FPS: int = 60  # 游戏帧率（帧/秒）

# =============================================================================
# 颜色定义 (RGB格式)
# =============================================================================

BLACK: tuple[int, int, int] = (0, 0, 0)  # 黑色
WHITE: tuple[int, int, int] = (255, 255, 255)  # 白色
RED: tuple[int, int, int] = (255, 0, 0)  # 红色
GREEN: tuple[int, int, int] = (0, 255, 0)  # 绿色
BLUE: tuple[int, int, int] = (0, 0, 255)  # 蓝色
YELLOW: tuple[int, int, int] = (255, 255, 0)  # 黄色
GRAY: tuple[int, int, int] = (128, 128, 128)  # 灰色
DARK_RED: tuple[int, int, int] = (150, 0, 0)  # 深红色

# =============================================================================
# 玩家飞机配置
# =============================================================================

PLAYER_WIDTH: int = 60  # 玩家飞机宽度（像素）
PLAYER_HEIGHT: int = 80  # 玩家飞机高度（像素）
PLAYER_SPEED: int = 5  # 玩家飞机移动速度（像素/帧）
PLAYER_INITIAL_LIVES: int = 9999999999  # 玩家初始生命值 - 无敌模式！
BULLET_COOLDOWN: float = 0.001  # 子弹发射冷却时间（秒）- 每秒1000发
AUTO_FIRE: bool = True  # 是否自动发射子弹

# =============================================================================
# 敌机配置
# =============================================================================

# 小型敌机配置
ENEMY_SMALL_WIDTH: int = 40  # 小型敌机宽度（像素）
ENEMY_SMALL_HEIGHT: int = 30  # 小型敌机高度（像素）
ENEMY_SMALL_SPEED: int = 2  # 小型敌机移动速度（像素/帧）
ENEMY_SMALL_HP: int = 1  # 小型敌机生命值
ENEMY_SMALL_SCORE: int = 10  # 击毁小型敌机获得的分数

# 中型敌机配置
ENEMY_MEDIUM_WIDTH: int = 60  # 中型敌机宽度（像素）
ENEMY_MEDIUM_HEIGHT: int = 45  # 中型敌机高度（像素）
ENEMY_MEDIUM_SPEED: int = 1  # 中型敌机移动速度（像素/帧）
ENEMY_MEDIUM_HP: int = 2  # 中型敌机生命值
ENEMY_MEDIUM_SCORE: int = 30  # 击毁中型敌机获得的分数

# =============================================================================
# 子弹配置
# =============================================================================

BULLET_WIDTH: int = 4  # 子弹宽度（像素）
BULLET_HEIGHT: int = 10  # 子弹高度（像素）
PLAYER_BULLET_SPEED: int = 8  # 玩家子弹移动速度（像素/帧）
ENEMY_BULLET_SPEED: int = 4  # 敌机子弹移动速度（像素/帧）

# =============================================================================
# 游戏机制配置
# =============================================================================

ENEMY_SPAWN_RATE: float = 0.02  # 敌机生成概率（每帧检查一次）
ENEMY_BULLET_RATE: float = 0.005  # 敌机发射子弹的基础概率（每帧检查一次）
ENEMY_MEDIUM_BULLET_MULTIPLIER: float = 2.0  # 中型敌机子弹发射概率倍数

# =============================================================================
# 音效配置
# =============================================================================

SOUND_ENABLED: bool = True  # 是否启用音效
SOUND_VOLUME: float = 0.5  # 音效音量 (0.0 - 1.0)
SHOOT_SOUND_INTERVAL: int = 5  # 射击音效播放间隔（每N发子弹播放一次音效）
